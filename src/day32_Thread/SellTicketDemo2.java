package day32_Thread;

/*
 * 实现Runnable接口的方式实现
 */
public class SellTicketDemo2 {
	public static void main(String[] args) {
		// 创建资源对象
		SellTicket2 st = new SellTicket2();

		// 创建三个线程对象
		Thread t1 = new Thread(st, "窗口1");
		Thread t2 = new Thread(st, "窗口2");
		Thread t3 = new Thread(st, "窗口3");

		// 启动线程
		t1.start();
		t2.start();
		t3.start();
	}
}